let a = 10;
let b = 5;

function adicao() {
    let soma = a + b;
    return soma;
}
console.log("Soma = " + adicao());

function subtracao() {
    let dimin = a - b;
    return dimin;
}
console.log("Subtração = " + subtracao());

function multiplicacao() {
    let multi = a * b;
    return multi;
}
console.log("Soma = " + multiplicacao());

function divisao() {
    let div = a / b;
    return div;
}
console.log("Divisão = " + divisao());

function modulo() {
    let mod = a % b;
    return mod;
}
console.log("Soma = " + modulo());
